# Una tupla es una coleccion ordenada e inmutable. Se escriben con ()
#---------------------------------------------------------------------

notastupla = (10, 3, 33, 54, 67, 58)
print(notastupla)

print(notastupla[1])

print(notastupla[-1])

print(notastupla[2:5])

print(notastupla[-4:-1])