
# Completa el script para mostrar distintos sectores de la listas "a, c, e, g e i"
# Si nos fijamos bien, seria hacer saltos de 2 en 2.

letters = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j"]
print(letters[::2])

# La sintaxis es [incio:final:salto]
# En nuetrado caso necesitamos hacer salto de 2 en 2 desde
# el inicio tenemos que hacerlo con [::2]